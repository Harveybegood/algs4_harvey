package Chapter1_1_BasicProgrammingModel;
/*
*   What do each of the following print?
*
* */
public class Ex08 {
    public static void main(String[] args) {
        System.out.println('b');  // char print char b
        System.out.println("b");     // String  print string b with the size of 1 array
        System.out.println('b' + 'c'); // arithmetic operations
        System.out.println("b" + "c");
        System.out.println((char)('a' + 4)); //explicit cast to char
    }
}
